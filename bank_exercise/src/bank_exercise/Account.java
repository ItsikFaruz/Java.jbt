package bank_exercise;

public class Account {

		int id;
		float balance;
	
		
		public Account(int id, float balance) {
			super();
			this.id = id;
			this.balance = balance;
		}


		public float getBalance() {
			return balance;
		}


		public void setBalance(float balance) {
			this.balance = balance;
		}


		public int getId() {
			return id;
		}
		
		
		
		
}
